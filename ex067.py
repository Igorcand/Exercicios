#TABUADA DE NUMEROS 

while True:
    n = int(input('Quer ver a tabuada de qual valor? '))
    if n < 0:
        break
    print(f'{n} X 1 = {n}')
    print(f'{n} X 2 = {n * 2}')
    print(f'{n} X 3 = {n * 3}')
    print(f'{n} X 4 = {n * 4}')
    print(f'{n} X 5 = {n * 5}')
    print(f'{n} X 6 = {n * 6}')
    print(f'{n} X 7 = {n * 7}')
    print(f'{n} X 8 = {n * 8}')
    print(f'{n} X 9 = {n * 9}')
    print(f'{n} X 10 = {n * 10}')
print('PROGARMA TABUADA ENCERRADO. Volte sempre!')